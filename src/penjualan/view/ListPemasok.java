    private void buttonKelolaDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonKelolaDataActionPerformed
        // TODO add your handling code here:
        int row = tabelPemasok.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(rootPane, "Pilih data pada tabel!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String idPemasok = tabelPemasok.getValueAt(row, 0).toString();
        try{
            session = SESSION_FACTORY.openSession();
            session.beginTransaction();
            pemasok = (Pemasok) session.createQuery("from Pemasok p where p.id=?").setParameter(0, idPemasok).uniqueResult();
        }catch(HibernateException he){
            session.getTransaction().rollback();
        }
        session.close();
        FORM_PEMASOK.setComponent(pemasok);
        this.dispose();
        FORM_PEMASOK.show();
    }//GEN-LAST:event_buttonKelolaDataActionPerformed

