    public void refreshTable(){
        try{
            session = SESSION_FACTORY.openSession();
            session.beginTransaction();
            List<Pemasok> pemasoks = session.createQuery("from Pemasok pemasok where pemasok.nama like ? order by pemasok.id asc").setParameter(0,"%"+textKataPencarian.getText()+"%").list();
            tabelPemasok.setModel(new PemasokTableModel(pemasoks));
        }catch(HibernateException e){
            e.printStackTrace();
        }
    }
    
