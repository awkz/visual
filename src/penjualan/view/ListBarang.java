    private void buttonKelolaBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonKelolaBarangActionPerformed
        // TODO add your handling code here:
        int row = tabelBarang.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(rootPane, "Pilih data pada table!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String idBarang = tabelBarang.getValueAt(row, 0).toString();
        try{
            session = SESSION_FACTORY.openSession();
            session.beginTransaction();
            barang = (Barang) session.createQuery("from Barang b where b.id=?").setParameter(0,idBarang).uniqueResult();
        }catch(HibernateException he){
            session.getTransaction().rollback();
        }
        session.close();
        FormBarang formBarang = new FormBarang(null, true);
        formBarang.setComponent(barang);
        this.dispose();
        formBarang.show();
    }//GEN-LAST:event_buttonKelolaBarangActionPerformed

