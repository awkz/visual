    public void refreshTable(){
        try{
            session = SESSION_FACTORY.openSession();
            session.beginTransaction();
            List<Barang> barangs = session.createQuery("from Barang barang where barang.nama Like ? order by barang.id asc").setParameter(0,"%"+textKataPencarian.getText()+"%").list();
            tabelBarang.setModel(new BarangTableModel(barangs));
        }catch(HibernateException e){
            e.printStackTrace();
        }
    }
    
